package lesson39_calculator;

public class Controller {

    int add(int x, int y) {
        return x + y;

    }

    double divide(int x, int y) {
        return (double) x / y;
    }

    int subtract(int x, int y) {
        return x - y;
    }

    int product (int x, int y) {
        return x * y;
    }
}
